  710       CONTINUE
         END IF

      ELSE IF (SETNO.EQ.8) THEN

C  SETNO. 8:  BATES AND WATTS, 1988, TABLE A1.13, PAGES 280-281
C             N.B.  THIS DERIVATIVE IS INTENTIONALLY CODED INCORRECTLY

         DO 800 I=1,N
            IF (XPLUSD(I,1).LT.0.0E0) THEN
               ISTOP = 1
               RETURN
            END IF
  800    CONTINUE
         ISTOP = 0
 
         IF (MOD(IDEVAL,10).NE.0) THEN
            PI = 3.141592653589793238462643383279E0
            THETA = PI*BETA(4)*0.5E0
            CTHETA = COS(THETA)
            STHETA = SIN(THETA)
            DO 810 I=1,N
               FREQ   = XPLUSD(I,1)
               OMEGA  = (2.0E0*PI*FREQ*EXP(-BETA(3)))**BETA(4)
               PHI    = ATAN2((OMEGA*STHETA),(1+OMEGA*CTHETA))
               R      = (BETA(1)-BETA(2)) *
     +                  SQRT((1+OMEGA*CTHETA)**2+(OMEGA*STHETA)**2)**
     +                  (-BETA(5))
               F(I,1) = BETA(2) + R*COS(BETA(5)*PHI)
               F(I,2) =           R*SIN(BETA(5)*PHI)
  810       CONTINUE
         END IF

         IF (MOD(IDEVAL/10,10).NE.0) THEN
            DO 820 I=1,N
               FJACB(I,1,1) = ZERO
               FJACB(I,2,1) = ZERO
               FJACB(I,3,1) = ZERO
               FJACB(I,4,1) = ZERO
               FJACB(I,5,1) = ZERO
   
               FJACB(I,1,2) = ZERO
               FJACB(I,2,2) = ZERO
               FJACB(I,3,2) = ZERO
               FJACB(I,4,2) = ZERO
               FJACB(I,5,2) = ZERO
   
               IF (MOD(IDEVAL/100,10).NE.0) THEN
                  FJACD(I,1,1) = ZERO
                  FJACD(I,1,2) = ZERO
               END IF
  820       CONTINUE
         END IF
      END IF

      RETURN

      END
