               DO 300 J=1,4
                  F(I,1) = F(I,1) + BETA(J)/(XPLUSD(I,1)+BETA(J+5))
  300          CONTINUE
               F(I,1) = F(I,1) + BETA(5)
  310       CONTINUE
         END IF

         IF (MOD(IDEVAL/10,10).NE.0) THEN
            DO 330 I=1,N
               FJACB(I,5,1) = ONE
               DO 320 K=1,4
                  FJACB(I,K,1) = ONE/(XPLUSD(I,1)+BETA(K+5))
                  FJACB(I,K+5,1) = -BETA(K)*
